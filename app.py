@app.route("/article/<category>/<int:part>")
def article(category, part):
    if "user" not in session:
        return redirect(url_for("home"))
    user = session["user"]
    path = ARTICLES_DIR / category / f"part-{part}.md"
    if not path.exists():
        return "Article not found", 404

    completed = get_completed(user)

    # enforce sequential unlock
    if part > 1 and (category, part - 1) not in completed:
        return redirect(url_for("dashboard"))

    art = parse_article_file(path)
    return render_template(
        "article.html",
        art=art,
        category=category,
        part=part,
        done=(category, part) in completed,
        theme=USERS[user]["theme"],
    )


@app.route("/complete/<category>/<int:part>", methods=["POST"])
def complete(category, part):
    if "user" not in session:
        return redirect(url_for("home"))
    mark_complete(session["user"], category, part)
    return redirect(url_for("dashboard"))


@app.route("/logout")
def logout():
    session.clear()
    return redirect(url_for("home"))


