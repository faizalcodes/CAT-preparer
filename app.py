@app.route("/api/quiz/generate", methods=["POST"])
def api_quiz_generate():
    if "user" not in session:
        return {"error": "not logged in"}, 401

    data = request.get_json(silent=True) or {}
    words = data.get("words", [])
    if not words:
        return {"error": "no words provided"}, 400

    try:
        questions = generate_quiz_questions(words)
        return {"questions": questions}
    except Exception as e:
        print("Quiz generation error:", e)
        return {"error": "quiz generation failed"}, 500


@app.route("/api/quiz/check", methods=["POST"])
def api_quiz_check():
    if "user" not in session:
        return {"error": "not logged in"}, 401

    data = request.get_json(silent=True) or {}
    word = (data.get("word") or "").strip()
    meaning = (data.get("meaning") or "").strip()
    sentence = (data.get("sentence") or "").strip()

    if not word or not sentence:
        return {"error": "missing word or sentence"}, 400
    if len(sentence) > 300:
        return {"error": "sentence too long"}, 400

    try:
        result = check_sentence_usage(word, meaning, sentence)
        return result
    except Exception as e:
        print("Quiz check error:", e)
        return {"error": "check failed"}, 500


@app.route("/dashboard")
def dashboard():
    if "user" not in session:
        return redirect(url_for("home"))
    user = session["user"]
    categories = load_all_articles()
    completed = get_completed(user)
    streak = get_streak(user)

    display = {}
    for cat, parts in categories.items():
        rows = []
        unlocked = True
        for a in parts:
            is_done = (cat, a["part"]) in completed
            rows.append({**a, "done": is_done, "unlocked": unlocked})
            if not is_done:
                unlocked = False
        display[cat] = rows

    return render_template(
        "dashboard.html",
        categories=display,
        streak=streak,
        user=USERS[user]["label"],
        theme=USERS[user]["theme"],
    )

